def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--public-manifest", default="benchmarks/module_compose_bench_manifest.yaml")
    parser.add_argument("--heldout-manifest", default="benchmarks/module_compose_bench_heldout.yaml")
    parser.add_argument("--realism-manifest", default="benchmarks/module_compose_bench_realism.yaml")
    parser.add_argument("--public-result", default="build/bench/seed_results.json")
    parser.add_argument("--heldout-result", default="build/bench/heldout_results.json")
    parser.add_argument("--realism-result", default="build/bench/realism_results.json")
    parser.add_argument("--llm-aggregate", default="build/bench/aggregate_llm_v4.json")
    parser.add_argument("--vivado-summary", default="build/reports/vivado-host/vivado_qor_subset_summary.json")
    parser.add_argument("--out-dir", default="build/paper_tables/paper_summary")
    args = parser.parse_args()

    aggregate = load_json(repo_path(args.llm_aggregate))
    out_dir = repo_path(args.out_dir)
    write_tex_table(
        out_dir / "static_checks.tex",
        [("check", "Check"), ("diagnostic", "Diagnostic codes"), ("evidence", "Evidence")],
        static_check_rows(),
    )
    write_tex_table(
        out_dir / "benchmark_split_summary.tex",
        [
            ("split", "Split"),
            ("tasks", "Tasks"),
            ("pos_neg", "Pos/Neg"),
            ("levels", "Levels"),
            ("case_pairs", "Cases/paired negatives"),
            ("expected", "Expected"),
            ("sim", "Simulation"),
            ("formal", "Formal"),
            ("qor", "QoR"),
        ],
        split_rows(args),
    )
    write_tex_table(
        out_dir / "llm_repair_boundary.tex",
        [
            ("path", "Path"),
            ("public", "Public positives"),
            ("heldout", "Held-out positives"),
            ("realism", "Realism positives"),
            ("wins", "Added wins"),
            ("provenance", "Provenance"),
        ],
        repair_rows(aggregate),
    )
    write_tex_table(
        out_dir / "llm_failure_taxonomy_compact.tex",
        [("baseline", "Baseline"), ("dominant", "Dominant events"), ("events", "Events")],
        failure_rows(aggregate),
    )
    write_tex_table(
        out_dir / "qor_scope_summary.tex",
        [("scope", "Scope"), ("rows", "Rows"), ("status", "Status"), ("claim", "Claim")],
        qor_rows(args),
    )
    print(f"wrote paper summary tables to {out_dir.relative_to(REPO_ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
